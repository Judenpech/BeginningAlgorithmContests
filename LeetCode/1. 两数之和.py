    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        n=len(nums)
        d={}
        for i in range(n):
            t=target-nums[i]
            if nums[i] in d:
                return d[nums[i]],i
            else:
                d[t]=i

